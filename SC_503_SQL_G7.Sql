CREATE TABLE ESQ_DIMENSIONES.DIM_UBICACION ( -- es para las ubicaciones que dan de clientes, de empleados,etc
    UBICACION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PAIS VARCHAR2(50)NOT NULL,
    PROVINCIA VARCHAR2(50)NOT NULL,
    CANTON VARCHAR2(50)NOT NULL,
    DISTRITO VARCHAR2(50)NOT NULL,
    CODIGO_POSTAL VARCHAR2(10),
    CONSTRAINT PK_DIM_UBICACION PRIMARY KEY(UBICACION_ID)
) TABLESPACE TS_FLORIEND_DATOS;

CREATE TABLE ESQ_DIMENSIONES.DIM_CLIENTE ( --aca di irian todos los datos del cliente
    CLIENTE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100)NOT NULL,
    APELLIDO VARCHAR2(100)NOT NULL,
    CORREO VARCHAR2(150) NOT NULL,
    TELEFONO VARCHAR2(20),
    UBICACION_ID NUMBER NOT NULL,
    FECHA_REGISTRO DATE DEFAULT SYSDATE,
    CONSTRAINT PK_DIM_CLIENTE PRIMARY KEY(CLIENTE_ID),
    CONSTRAINT UQ_DIM_CLIENTE_CORREO UNIQUE(CORREO),
    CONSTRAINT FK_CLIENTE_UBICACION FOREIGN KEY(UBICACION_ID)REFERENCES ESQ_DIMENSIONES.DIM_UBICACION(UBICACION_ID)
) TABLESPACE TS_FLORIEND_DATOS;

CREATE TABLE ESQ_DIMENSIONES.DIM_EMPLEADO ( -- aca tambien lo mismo pero de empleado xd
    EMPLEADO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100)NOT NULL,
    APELLIDO VARCHAR2(100)NOT NULL,
    CORREO VARCHAR2(150) NOT NULL,
    TELEFONO VARCHAR2(20),
    CARGO VARCHAR2(50)NOT NULL,
    UBICACION_ID NUMBER NOT NULL,
    FECHA_INGRESO DATE NOT NULL,
    CONSTRAINT PK_DIM_EMPLEADO PRIMARY KEY (EMPLEADO_ID),
    CONSTRAINT UQ_DIM_EMPLEADO_CORREO UNIQUE (CORREO),
    CONSTRAINT FK_EMPLEADO_UBICACION FOREIGN KEY (UBICACION_ID)REFERENCES ESQ_DIMENSIONES.DIM_UBICACION (UBICACION_ID)
) TABLESPACE TS_FLORIEND_DATOS;

CREATE TABLE ESQ_DIMENSIONES.DIM_PROVEEDOR ( -- same con los proveedores
    PROVEEDOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(150) NOT NULL,
    CORREO VARCHAR2(150),
    TELEFONO VARCHAR2(20),
    UBICACION_ID NUMBER NOT NULL,
    CONSTRAINT PK_DIM_PROVEEDOR PRIMARY KEY (PROVEEDOR_ID),
    CONSTRAINT FK_PROVEEDOR_UBICACION FOREIGN KEY (UBICACION_ID)REFERENCES ESQ_DIMENSIONES.DIM_UBICACION (UBICACION_ID)
) TABLESPACE TS_FLORIEND_DATOS;

CREATE TABLE ESQ_DIMENSIONES.DIM_MARCA ( --marca del producto
    MARCA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    DESCRIPCION VARCHAR2(255),
    CONSTRAINT PK_DIM_MARCA PRIMARY KEY (MARCA_ID)
) TABLESPACE TS_FLORIEND_DATOS;

CREATE TABLE ESQ_DIMENSIONES.DIM_CATEGORIA ( -- a cual categoria pertenece el producto
    CATEGORIA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    DESCRIPCION VARCHAR2(255),
    CONSTRAINT PK_DIM_CATEGORIA PRIMARY KEY (CATEGORIA_ID)
) TABLESPACE TS_FLORIEND_DATOS;

CREATE TABLE ESQ_DIMENSIONES.DIM_DESCUENTO ( --los descuentos que van a estar
    DESCUENTO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    PORCENTAJE NUMBER(5,2)NOT NULL,
    FECHA_INICIO DATE NOT NULL,
    FECHA_FIN DATE NOT NULL,
    CONSTRAINT PK_DIM_DESCUENTO PRIMARY KEY (DESCUENTO_ID),
    CONSTRAINT CHK_DESCUENTO_PORCENTAJE CHECK (PORCENTAJE > 0 AND PORCENTAJE <= 100),
    CONSTRAINT CHK_FECHA_DESCUENTO CHECK (FECHA_INICIO <= FECHA_FIN)
) TABLESPACE TS_FLORIEND_DATOS;

