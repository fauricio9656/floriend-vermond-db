CREATE TABLE ESQ_TRANSACCIONAL.PRODUCTO ( --productos que van a ver y aqui estaran
    PRODUCTO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(150)NOT NULL,
    DESCRIPCION VARCHAR2(255),
    PRECIO NUMBER(10,2)NOT NULL,
    CANTIDAD_DISP NUMBER NOT NULL,
    MARCA_ID NUMBER NOT NULL,
    CATEGORIA_ID NUMBER NOT NULL,
    DESCUENTO_ID NUMBER,
    CONSTRAINT PK_PRODUCTO PRIMARY KEY (PRODUCTO_ID),
    CONSTRAINT CHK_PRODUCTO_PRECIO CHECK (PRECIO > 0),
    CONSTRAINT CHK_PRODUCTO_CANTIDAD CHECK (CANTIDAD_DISP >= 0),
    CONSTRAINT FK_PRODUCTO_MARCA FOREIGN KEY (MARCA_ID)REFERENCES ESQ_DIMENSIONES.DIM_MARCA (MARCA_ID),
    CONSTRAINT FK_PRODUCTO_CATEGORIA FOREIGN KEY (CATEGORIA_ID) REFERENCES ESQ_DIMENSIONES.DIM_CATEGORIA (CATEGORIA_ID),
    CONSTRAINT FK_PRODUCTO_DESCUENTO FOREIGN KEY (DESCUENTO_ID) REFERENCES ESQ_DIMENSIONES.DIM_DESCUENTO (DESCUENTO_ID)
) TABLESPACE TS_FLORIEND_DATOS;

CREATE TABLE ESQ_TRANSACCIONAL.INVENTARIO (
    INVENTARIO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PRODUCTO_ID NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    FECHA_ULTIMO_MOV DATE DEFAULT SYSDATE,
    CONSTRAINT PK_INVENTARIO PRIMARY KEY (INVENTARIO_ID),
    CONSTRAINT CHK_INVENTARIO_CANTIDAD CHECK (CANTIDAD >= 0),
    CONSTRAINT FK_INVENTARIO_PRODUCTO FOREIGN KEY (PRODUCTO_ID) REFERENCES ESQ_TRANSACCIONAL.PRODUCTO (PRODUCTO_ID)
) TABLESPACE TS_FLORIEND_DATOS;
CREATE TABLE ESQ_TRANSACCIONAL.PEDIDO_HECHO ( -- aca se mostrara el pedido que se realizo tanto para el cliente como el empleado
    PEDIDO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CLIENTE_ID NUMBER NOT NULL,
    FECHA_PEDIDO DATE DEFAULT SYSDATE,
    TOTAL NUMBER(10,2)NOT NULL,
    CONSTRAINT PK_PEDIDO PRIMARY KEY (PEDIDO_ID),
    CONSTRAINT CHK_PEDIDO_TOTAL CHECK (TOTAL > 0),
    CONSTRAINT FK_PEDIDO_CLIENTE FOREIGN KEY (CLIENTE_ID) REFERENCES ESQ_DIMENSIONES.DIM_CLIENTE (CLIENTE_ID)
) TABLESPACE TS_FLORIEND_DATOS;
CREATE TABLE ESQ_TRANSACCIONAL.DETALLE_PEDIDO ( -- es como un previo para que vean que se comrpo
    DETALLE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PEDIDO_ID NUMBER NOT NULL,
    PRODUCTO_ID NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    PRECIO_UNIT NUMBER(10,2) NOT NULL,
    CONSTRAINT PK_DETALLE_PEDIDO PRIMARY KEY (DETALLE_ID),
    CONSTRAINT CHK_DETALLE_CANTIDAD CHECK (CANTIDAD > 0),
    CONSTRAINT CHK_DETALLE_PRECIO CHECK (PRECIO_UNIT > 0),
    CONSTRAINT FK_DETALLE_PEDIDO FOREIGN KEY (PEDIDO_ID) REFERENCES ESQ_TRANSACCIONAL.PEDIDO_HECHO(PEDIDO_ID),
    CONSTRAINT FK_DETALLE_PRODUCTO FOREIGN KEY (PRODUCTO_ID) REFERENCES ESQ_TRANSACCIONAL.PRODUCTO(PRODUCTO_ID)
) TABLESPACE TS_FLORIEND_DATOS;
CREATE TABLE ESQ_TRANSACCIONAL.DEVOLUCION ( -- en caso de algun cliente insatisfecho jajaja
    DEVOLUCION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PEDIDO_ID NUMBER NOT NULL,
    FECHA_DEVOLUCION DATE DEFAULT SYSDATE,
    MOTIVO VARCHAR2(255),
    CONSTRAINT PK_DEVOLUCION PRIMARY KEY (DEVOLUCION_ID),
    CONSTRAINT FK_DEVOLUCION_PEDIDO FOREIGN KEY (PEDIDO_ID) REFERENCES ESQ_TRANSACCIONAL.PEDIDO_HECHO(PEDIDO_ID)
) TABLESPACE TS_FLORIEND_DATOS;

