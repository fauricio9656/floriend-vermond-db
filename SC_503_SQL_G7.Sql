CREATE MATERIALIZED VIEW ESQ_TRANSACCIONAL.VM_VENTAS_CLIENTE
REFRESH FORCE
ON DEMAND
AS
SELECT c.CLIENTE_ID,c.NOMBRE,c.APELLIDO,
    COUNT(p.PEDIDO_ID) AS TOTAL_PEDIDOS,
    SUM(p.TOTAL) AS MONTO_TOTAL
FROM ESQ_DIMENSIONES.DIM_CLIENTE c
JOIN ESQ_TRANSACCIONAL.PEDIDO_HECHO p ON c.CLIENTE_ID = p.CLIENTE_ID
GROUP BY c.CLIENTE_ID, c.NOMBRE, c.APELLIDO;

--para ver los top globales de productos jaja
CREATE MATERIALIZED VIEW ESQ_TRANSACCIONAL.VM_TOP_PRODUCTOS
REFRESH FORCE
ON DEMAND
AS
SELECT pr.PRODUCTO_ID,
       pr.NOMBRE AS PRODUCTO,
       m.NOMBRE AS MARCA,
       SUM(dp.CANTIDAD) AS TOTAL_UNIDADES,
       SUM(dp.CANTIDAD * dp.PRECIO_UNIT) AS INGRESO_TOTAL
FROM ESQ_TRANSACCIONAL.PRODUCTO pr
JOIN ESQ_TRANSACCIONAL.DETALLE_PEDIDO dp ON pr.PRODUCTO_ID = dp.PRODUCTO_ID
JOIN ESQ_DIMENSIONES.DIM_MARCA m ON pr.MARCA_ID = m.MARCA_ID
GROUP BY pr.PRODUCTO_ID, pr.NOMBRE, m.NOMBRE;

